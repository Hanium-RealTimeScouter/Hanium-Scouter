package scouter.server.db.io.zip;

public class BKey {
	public String date;
	public int blockNum;

	public BKey(String date, int blockNum) {
		this.date = date;
		this.blockNum = blockNum;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + blockNum;
		result = prime * result + ((date == null) ? 0 : date.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		BKey other = (BKey) obj;
		if (blockNum != other.blockNum)
			return false;
		if (date == null) {
			if (other.date != null)
				return false;
		} else if (!date.equals(other.date))
			return false;
		return true;
	}

	

}