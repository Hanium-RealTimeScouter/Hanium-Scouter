package scouter.client.model;


public class GroupObject extends HierarchyObject {
	private String name;
	private String objType;
	private HierarchyObject firstChild;
	
	public GroupObject(String objType, String name) {
		this.objType = objType;
		this.name = name;
	}
	
	public String getObjType() {
		return this.objType;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getName() {
		return this.name;
	}

	public HierarchyObject putChild(String key, HierarchyObject child) {
		if (getChildSize() == 0) {
			firstChild = child;
		}
		return super.putChild(key, child);
	}
	
	public HierarchyObject getFirstChild() {
		return firstChild;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		result = prime * result + ((objType == null) ? 0 : objType.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		GroupObject other = (GroupObject) obj;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		if (objType == null) {
			if (other.objType != null)
				return false;
		} else if (!objType.equals(other.objType))
			return false;
		return true;
	}

	public String getDisplayName() {
		return name;
	}

}
